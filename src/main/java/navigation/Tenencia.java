public class Tenencia {
    private String fondo;
    private String cuotapartes;
    private String ultimaCotizacion;
    private String pesos;
    private String dolares;

    public String getFondo() {
        return fondo;
    }

    public void setFondo(String fondo) {
        this.fondo = fondo;
    }

    public String getCuotapartes() {
        return cuotapartes;
    }

    public void setCuotapartes(String cuotapartes) {
        this.cuotapartes = cuotapartes;
    }

    public String getUltimaCotizacion() {
        return ultimaCotizacion;
    }

    public void setUltimaCotizacion(String ultimaCotizacion) {
        this.ultimaCotizacion = ultimaCotizacion;
    }

    public String getPesos() {
        return pesos;
    }

    public void setPesos(String pesos) {
        this.pesos = pesos;
    }

    public String getDolares() {
        return dolares;
    }

    public void setDolares(String dolares) {
        this.dolares = dolares;
    }
}
